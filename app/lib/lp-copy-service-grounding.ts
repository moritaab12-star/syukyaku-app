/**
 * LP コピー生成（Gemini）向け: サービス原文を唯一の業種定義とし、汎用定型を抑えるプロンプト断片。
 */

/** 未設定プレースホルダ（プロンプト内） */
const UNSET_SERVICE = '（未設定）';

/**
 * サービス欄の自由記述を「業種の定義」として縛るルール文。
 */
export function buildServiceGroundingRulesForPrompt(serviceRaw: string): string {
  const s = serviceRaw.normalize('NFKC').trim();
  const hasContent = s.length > 0 && s !== UNSET_SERVICE;

  const lines: string[] = [
    '【サービス原文＝このLPの業種・事業の唯一の定義】',
  ];

  if (hasContent) {
    lines.push(
      '次に提示する「対応サービス・業種の原文」を **全文読み、語句ごとに意味を解釈** し、そこに書かれている事業・メニュー・商材・作業内容のみを「このLPが扱う範囲」とみなす。',
      '- **原文に現れない業種・工事・商品・食材の語**は、見出し・リード・箇条書き・CTA補足のいずれにも **使わない**（例: 原文が外壁と水道なら、剪定・賃貸仲介・カステラ等は禁止）。',
      '- 原文が「A、B、C」のように複数が並ぶ場合、**その複数を偏りなく反映**するか、無理に一業種に寄せずバランスよく触れる。',
      '- 補助の「業種キー」「業種トーン説明」は**原文と矛盾する場合は無条件で原文側**を採用する。',
    );
  } else {
    lines.push(
      'サービス原文が未設定のため業種を特定できない。超短文・中立的な言い回しに留め、**具体的な工事名・商品名・業態名の断定はしない**。',
    );
  }

  lines.push(
    '',
    '【アンケート50問との関係】',
    '- **数値実績・保証内容・固有名・お客様の声の具体的記述**は、**アンケートに書かれた内容があればそれを最優先**（無いものは捏造しない）。',
    '- 設問が **未入力・流用しにくいブロック**でも、**文のトピック・語彙・悩みの例**は必ず **サービス原文の業種・業務に沿わせる**。「どの街のどんな店でも使える」抽象文**だけ**で埋めてはならない。',
    '',
    '【汎用LPとの差別化（禁止寄り）】',
    '- 「地域密着」「お困りごと」「まずはお気軽に」「安心のサポート」「丁寧に対応」**などのフレーズだけ**で段落や3連バッジを構成してはならない。**必ずサービス原文から取った語（漢字のまま、または自然な短縮）を複数フィールドに分散して含める**。',
    '- 他業種の定型（例: 〇年の実績は数値が無ければ書かない、植木屋・塗装の例えを原文がそうでなければ使わない）。',
  );

  return lines.join('\n');
}
